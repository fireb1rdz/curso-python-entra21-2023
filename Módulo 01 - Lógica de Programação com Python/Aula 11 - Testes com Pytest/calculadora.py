"""
Módulo responsável por implementar as funções de uma calculadora.
"""


def dividir(n1: int, n2: int) -> float:
    """Retorna o quociente de uma divisão."""
    return n1 / n2


def somar(n1: int, n2: int) -> int:
    """Soma dois números inteiros."""
    return n1 + n2


def subtrair(n1: int, n2: int) -> int:
    """Subtrai dois números inteiros."""
    return n1 - n2


def multiplicar(n1: int, n2: int) -> int:
    """Multiplica dois números."""
    return n1 * n2